    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        // TODO add your handling code here:
        try {
            String user = username.getText();
            String level, id;
            String sql = "SELECT * FROM taufik_user WHERE username='"+username.getText()+"' AND password='"+password.getText()+"'";
            java.sql.Connection conn=(Connection)Config.configDB();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(sql);
            if(res.next()){
                if(username.getText().equals(res.getString("username")) && password.getText().equals(res.getString("password"))){
                    level = res.getString("level");
                    id = res.getString("id_user");
                    new Dashboard(id, level).show();
                    this.dispose();
                }
            }else{
                JOptionPane.showMessageDialog(null, "username atau password salah");
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_loginActionPerformed

